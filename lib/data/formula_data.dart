  /// Get formulas for a specific subject
  static List<Formula> getFormulasBySubject(FormulaSubject subject) {
    return getAllFormulas().where((f) => f.subject == subject).toList();
  }

  /// Get formulas for a specific topic
  static List<Formula> getFormulasByTopic(String topic) {
    return getAllFormulas().where((f) => f.topic == topic).toList();
  }

  /// Get all unique topics for a subject
  static List<String> getTopicsForSubject(FormulaSubject subject) {
    final formulas = getFormulasBySubject(subject);
    final topics = formulas.map((f) => f.topic).toSet().toList();
    topics.sort();
    return topics;
  }

  /// Get all unique subjects
  static List<FormulaSubject> getAllSubjects() {
    return FormulaSubject.values;
  }
